package com.argusoft.armms.web.usermanagement.databean;

import javax.faces.bean.ManagedBean;
import javax.faces.bean.RequestScoped;

/**
 * Data Bean fro permission 
 * @author rajkumar
 * @since 08-APR-2014
 */
@ManagedBean(name = "permissionDataBean")
@RequestScoped
public class PermissionDataBean {
    private Long ID;
    private String name;
    private String[] attributes;
    private Long addNew;
    public Long getID() {
        return ID;
    }

    public void setID(Long ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String[] getAttributes() {
        return attributes;
    }

    public void setAttributes(String[] attributes) {
        this.attributes = attributes;
    }

    public Long getAddNew() {
        return addNew;
    }

    public void setAddNew(Long addNew) {
        this.addNew = addNew;
    }
    
    
}
