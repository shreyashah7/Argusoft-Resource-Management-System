package com.argusoft.armms.web.usermanagement.databean;

import javax.faces.bean.ManagedBean;
import javax.faces.bean.RequestScoped;

/**
 * Data bean for feature
 * @author rajkumar
 * @since 08-APR-2014
 */
@ManagedBean(name = "featureDataBean")
@RequestScoped
public class FeatureDataBean {
    private Long id;
    private String name;
    private Boolean enableDisable;
    private Long parent;
    private String menuLable;
    private String description;
    private Integer precedence;
    private String menuType;
    private String featureURL;
    private String webserviceURL;
    private Integer seqNo;
    private Boolean crud;
    private Boolean active;
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getEnableDisable() {
        return enableDisable;
    }

    public void setEnableDisable(Boolean enableDisable) {
        this.enableDisable = enableDisable;
    }

    public Long getParent() {
        return parent;
    }

    public void setParent(Long parent) {
        this.parent = parent;
    }

    public String getMenuLable() {
        return menuLable;
    }

    public void setMenuLable(String menuLable) {
        this.menuLable = menuLable;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Integer getPrecedence() {
        return precedence;
    }

    public void setPrecedence(Integer precedence) {
        this.precedence = precedence;
    }

    public String getMenuType() {
        return menuType;
    }

    public void setMenuType(String menuType) {
        this.menuType = menuType;
    }

    public String getFeatureURL() {
        return featureURL;
    }

    public void setFeatureURL(String featureURL) {
        this.featureURL = featureURL;
    }

    public String getWebserviceURL() {
        return webserviceURL;
    }

    public void setWebserviceURL(String webserviceURL) {
        this.webserviceURL = webserviceURL;
    }

    public Integer getSeqNo() {
        return seqNo;
    }

    public void setSeqNo(Integer seqNo) {
        this.seqNo = seqNo;
    }

    public Boolean getCrud() {
        return crud;
    }

    public void setCrud(Boolean crud) {
        this.crud = crud;
    }

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }
    
}
