package com.argusoft.armms.web.databean;

/**
 * this class is for putting details of a file/folder (for folder structure)
 * @author ravi
 */
public class FolderDataBean {
    int level;
    String key;
    String type;
    String keyType;

    public String getKeyType() {
        return keyType;
    }

    public void setKeyType(String keyType) {
        this.keyType = keyType;
    }
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }        

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    @Override
    public String toString() {
        return "level=" + level + " key=" + key + " type=" + type + " keyType=" + keyType;
    }

   

    
    
    
}
